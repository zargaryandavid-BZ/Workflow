import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

// Dates are formatted with a fixed locale and timezone (UTC) so the output is
// identical on the server and the client. Using the runtime default locale or
// timezone causes React hydration mismatches (server runs in UTC, the browser
// in the user's local zone).
export function formatDate(value?: string | null) {
  if (!value) return "";
  const d = new Date(value);
  if (Number.isNaN(d.getTime())) return "";
  return d.toLocaleDateString("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
    timeZone: "UTC",
  });
}

export function formatDateTime(value?: string | null) {
  if (!value) return "";
  const d = new Date(value);
  if (Number.isNaN(d.getTime())) return "";
  return d.toLocaleString("en-US", {
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
    timeZone: "UTC",
  });
}

export function initials(name?: string | null) {
  if (!name) return "?";
  return name
    .split(" ")
    .filter(Boolean)
    .slice(0, 2)
    .map((p) => p[0]?.toUpperCase())
    .join("");
}

/** YYYY-MM-DD for `<input type="date">` in the user's local timezone. */
export function localDateInputValue(date = new Date()): string {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${y}-${m}-${d}`;
}

/** Normalize ISO or date strings to YYYY-MM-DD for date inputs. */
export function dateInputValue(value?: string | null): string {
  if (!value) return "";
  return value.trim().slice(0, 10);
}

export function isPastDateInputValue(value: string): boolean {
  return dateInputValue(value) < localDateInputValue();
}
